int valAng(char as[]){
	int cek = 0;
   if(as[0] == NULL){
   	return 1;
   } else {
   	for(int i=0; as[i]!=NULL; i++){
   		if(!(as[i]>='0' && as[i]<='9')){
      		cek++;
      	}
      }
   }
   return cek;
}

//==============================================================================
//                          VALIDASI HURUF
//==============================================================================
int valHur(char as[]){
	int cek = 0;
   if(as[0] == NULL || as[0] == ' '){
   	return 1;
   } else {
   	for(int i=0; as[i]!=NULL; i++){
   		if(!(as[i]>='a' && as[i]<='z' || as[i]>='A' && as[i]<='Z' || as[0] == ' ')){
      		cek++;
      	}
      }
   }
   return cek;
}

